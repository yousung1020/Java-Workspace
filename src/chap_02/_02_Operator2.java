package chap_02;

public class _02_Operator2 {
    public static void main(String[] args) {
        // 대입 연산자

        int num = 10;
        num = num + 2; // 12
        System.out.println(num);

        // 복합 대입 연산자
        // +=, -=, /=, %=, *=, etc..

        num += 2; // 14
        System.out.println(num);

    }
}
