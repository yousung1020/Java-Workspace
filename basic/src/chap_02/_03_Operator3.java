package chap_02;

public class _03_Operator3 {
    public static void main(String[] args) {
        // 비교 연산자

        System.out.println(5 > 3);
        System.out.println(5 < 3);
        System.out.println(5 >= 3); // 5가 3보다 크거나 같다.
        System.out.println(5 >= 5);
        System.out.println(5 != 4); // 5는 4와 같지 않다.


    }
}
